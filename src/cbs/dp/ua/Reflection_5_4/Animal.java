package cbs.dp.ua.Reflection_5_4;

public class Animal {

    private String name = "Vasyka";
    private int age = 3;
    private double height = 2.37;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }
}
